package ru.bidonov.task6.Calculator;

public class Calculator {

    static double doublePlus (double a, double b) {
        return a + b;
    }

    static double doubleMinus (double a, double b) {
        return a - b;
    }

    static double doubleMultiplication (double a, double b) {
        return a * b;
    }

    static double doubleDividing (double a, double b) {
        return a / b;
    }

    static double doublePercent (double a, double b) {
        return a * b / 100;
    }

    static int intPlus (int a, int b) {
        return a + b;
    }

    static int intMinus (int a, int b) {
        return a - b;
    }

    static int intMultiplication (int a, int b) {
        return a * b;
    }

    static int intDividing (int a, int b) {
        return a / b;
    }

    static int intPercent (int a, int b) {
        return a * b / 100;
    }
}




